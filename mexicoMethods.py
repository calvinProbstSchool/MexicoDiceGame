def playRound() :
    mexicoRound = False
    print("Rolling Dice... ")
    print("Results: ")
    loserList = [ 0 ]
    lowPlayerRank = 0
    for i in playerList :
        if i != 0 :
            dice1.roll()
            rollN1 = dice1.faceValue()
            dice1.roll()
            rollN2 = dice1.faceValue()
            print("Player " + str( i ) + "rolled a " +
                         str( rollN1 ) + "and a " + str( rollN2 ) )
            activeRank = rollRanker( rollN1, rollN2 )
            if activeRank == 1 :
                print("Player "+ str( i ) + "threw Mexico!")
                mexicoRound = True
            if lowPlayerRank < activeRank :
                lowPlayerRank = activeRank
                loserList = [ i ]
            elif lowplayerRank == activeRank :
                loserList.append( i )
    for loser in loserList :
        print("Player " + str( loser ) + " is a big loser")
        if mexicoRound :
            livesList[ loser - 1 ] = livesList[ loser - 1 ] - 2
            print("Player " + str( loser ) +
                  "lost 2 life because Mexico was thrown this round")
        else :
            livesList[ loser - 1 ] = livesList[ loser - 1 ] - 1
        print("Player " + str( loser ) +
                  "lost 1 life")
        if livesList[ loser - 1 ] < 1 :
            print("Player " + str( loser ) + " died! only " +
                  str( numPlayers - 1 ) + " left!")
            playerList[ loser - 1 ] = 0
            numPlayers = numPlayers - 1
            if numPlayers == 1 :
                for winner in playerList :
                    if winner != 0 :
                        print("Player " + str( winner ) + " won!!!")
                        printTrophy()
            elif numPlayers < 1 :
                print("No winners today! It's a dead game!")
    if numPlayers > 1 :
        printScoreBoard()
        playRound()
        
            
    
            
                
                
                
            










    
